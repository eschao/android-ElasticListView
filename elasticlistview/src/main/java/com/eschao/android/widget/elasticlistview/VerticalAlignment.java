package com.eschao.android.widget.elasticlistview;

/**
 * Vertical alignment of HeaderView or FooterView
 *  
 * @author eschao
 */
public enum VerticalAlignment {

	/**
	 * The content view is at the top of its parent
	 */
	TOP,
	
	/**
	 * The content view is center in its parent
	 */
	CENTER,
	
	/**
	 * The content view is at the bottom of its parent
	 */
	BOTTOM
}
